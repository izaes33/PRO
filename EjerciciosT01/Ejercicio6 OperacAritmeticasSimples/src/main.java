public class main {
    public static void main(String[] args) {
        int primerValor = 9;
        int segundoValor = 3;

        System.out.println("Primer Valor: " + primerValor);
        System.out.println("Segundo Valor: " + segundoValor);

        int resultadoSuma = primerValor + segundoValor;
        int resultadoResta = primerValor - segundoValor;
        int resultadoMultiplicación = primerValor * segundoValor;
        int resultadoDivisión = primerValor / segundoValor;

        System.out.println("Resultado Suma: " + resultadoSuma);
        System.out.println("Resultado Resta: " + resultadoResta);
        System.out.println("Resultado Multiplicación: " + resultadoMultiplicación);
        System.out.println("Resultado División: " + resultadoDivisión);

    }
}
